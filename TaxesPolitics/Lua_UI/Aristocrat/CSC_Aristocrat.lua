function CSCAristocrat(iPlayerID : number, tParams : table)
	
    local pCity = CityManager.GetCity(iPlayerID, tParams.iCityID)
	pCity:AttachModifierByID("MOD_CSC_GRANT_ARISTOCRAT_ATTACH")

end

GameEvents.CSC_GrantAristocrat.Add(CSCAristocrat);

